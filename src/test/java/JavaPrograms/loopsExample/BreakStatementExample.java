package JavaPrograms.loopsExample;

public class BreakStatementExample {

	public static void main(String[] args) {
		  for(int i=1;i<=10;i++){  
		        if(i==5){  
		            //breaking the loop  
		            break;  
		        }  
		        System.out.println(i);  
		    }  

	}

}
