package JavaPrograms.ConditionalStatements;

public class IfCondition {

	public static void main(String[] args) {
		//defining an 'age' variable  
	    int age=20;  
	    //checking the age  
	    if(age>18){  
	        System.out.print("Age is greater than 18");  
	    } 
	    
	    System.out.println("Program End");
	}
}

